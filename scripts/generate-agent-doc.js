#!/usr/bin/env node
/**
 * Generate a raw markdown copy of the Agentic Coding reference for direct
 * AI ingestion (curl, llms.txt consumers, fetch tools).
 *
 * Reads:  content/documentation/reference/agentic-coding.md
 * Writes: static/agentic-coding.md
 *
 * Strips the Zola TOML frontmatter. The file uses no shortcodes, so the body
 * passes through verbatim.
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const SOURCE = path.join(ROOT, 'content/documentation/reference/agentic-coding.md');
const OUT_MD = path.join(ROOT, 'static/agentic-coding.md');

function stripFrontmatter(raw) {
  const match = raw.match(/^\+\+\+\n[\s\S]*?\n\+\+\+\n+/);
  if (!match) return raw;
  return raw.slice(match[0].length);
}

function buildHeader(sourceUrl) {
  return [
    '# Phel: Agentic Coding Reference',
    '',
    `> Plain-markdown export of ${sourceUrl}.`,
    '> Generated by scripts/generate-agent-doc.js. Edit the source, not this file.',
    '',
    '---',
    '',
  ].join('\n');
}

function main() {
  const raw = fs.readFileSync(SOURCE, 'utf8');
  const body = stripFrontmatter(raw);
  const header = buildHeader('https://phel-lang.org/documentation/reference/agentic-coding/');
  const output = header + body;

  fs.writeFileSync(OUT_MD, output);

  const bytes = Buffer.byteLength(output, 'utf8');
  console.log(`Wrote ${OUT_MD} (${bytes} bytes)`);
}

main();
