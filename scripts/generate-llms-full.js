#!/usr/bin/env node
/**
 * Generate static/llms-full.txt: a single plain-markdown bundle of the key
 * documentation pages, for LLM consumers that want the whole reference in one
 * fetch (the full-content companion to static/llms.txt).
 *
 * Reads:  the curated PAGES list below (content/**.md)
 * Writes: static/llms-full.txt
 *
 * Each page has its Zola TOML frontmatter and Tera shortcodes stripped, then is
 * emitted under an `# <title>` header with its canonical source URL.
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const BASE_URL = 'https://phel-lang.org';
const OUT = path.join(ROOT, 'static/llms-full.txt');

// Curated, ordered. Paths are relative to content/. Keep the agent reference
// first so a truncated read still gets the highest-value page.
const PAGES = [
  'documentation/reference/agentic-coding.md',
  'documentation/reference/cheat-sheet.md',
  'documentation/language/basic-types.md',
  'documentation/language/data-structures.md',
  'documentation/language/functions-and-recursion.md',
  'documentation/language/control-flow.md',
  'documentation/language/destructuring.md',
  'documentation/language/error-handling.md',
  'documentation/language/namespaces.md',
  'documentation/language/macros.md',
  'documentation/php-interop.md',
  'documentation/tooling/cli-commands.md',
  'documentation/tooling/repl.md',
  'documentation/testing.md',
  'documentation/reference/errors.md',
];

function splitFrontmatter(raw) {
  const match = raw.match(/^\+\+\+\n([\s\S]*?)\n\+\+\+\n+/);
  if (!match) return { frontmatter: '', body: raw };
  return { frontmatter: match[1], body: raw.slice(match[0].length) };
}

function titleFrom(frontmatter, fallback) {
  const m = frontmatter.match(/^title\s*=\s*"([^"]*)"/m);
  return m ? m[1] : fallback;
}

// Drop Tera shortcodes: paired `{% name(...) %}...{% end %}` keep their inner
// text (just remove the tags), and `{{ ... }}` variables are removed outright.
function stripShortcodes(body) {
  return body
    .replace(/\{%[^%]*%\}/g, '')
    .replace(/\{\{[^}]*\}\}/g, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

function urlFor(relPath) {
  const slug = relPath.replace(/\.md$/, '').replace(/\/_index$/, '');
  return `${BASE_URL}/${slug}/`;
}

function main() {
  const parts = [
    '# Phel: Full Documentation Bundle',
    '',
    '> Plain-markdown bundle of the core Phel documentation for LLM ingestion.',
    '> Generated by scripts/generate-llms-full.js. Edit the source pages, not this file.',
    `> Index and links: ${BASE_URL}/llms.txt`,
    '',
    '---',
    '',
  ];

  for (const relPath of PAGES) {
    const abs = path.join(ROOT, 'content', relPath);
    if (!fs.existsSync(abs)) {
      throw new Error(`generate-llms-full: missing source page ${relPath}`);
    }
    const raw = fs.readFileSync(abs, 'utf8');
    const { frontmatter, body } = splitFrontmatter(raw);
    const title = titleFrom(frontmatter, relPath);
    parts.push(
      `# ${title}`,
      '',
      `> Source: ${urlFor(relPath)}`,
      '',
      stripShortcodes(body),
      '',
      '---',
      '',
    );
  }

  const output = parts.join('\n');
  fs.writeFileSync(OUT, output);
  console.log(`Wrote ${OUT} (${Buffer.byteLength(output, 'utf8')} bytes, ${PAGES.length} pages)`);
}

main();
